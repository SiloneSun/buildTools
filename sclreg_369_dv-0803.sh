catInfo() {
    info="$1"  # 获取传入的信息
    # 使用正则表达式将信息中的数字部分替换为空，以获取不含数字的部分作为上级目录名称
    parent_dir=$(echo "$info" | sed 's/[0-9]*//g')
    module_path="/proc/mi_modules/${parent_dir}/${info}"  # 构建路径
    echo "cat ${module_path}"  # 打印执行的命令
    cat "${module_path}"  # 执行命令
    sleep 0.1
    echo "cat ${module_path}"  # 再次打印执行的命令
    cat "${module_path}"  # 再次执行命令
    sleep 0.1
    echo "cat ${module_path}"  # 第三次打印执行的命令
    cat "${module_path}"  # 第三次执行命令
    sleep 0.1
}

